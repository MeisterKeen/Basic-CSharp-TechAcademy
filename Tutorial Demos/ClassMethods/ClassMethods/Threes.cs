using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassMethods
{
    public class Threes
    {
        public static int Divide(int num1)
        {
            return num1 / 3;
        }

        public static int Modulus(int num1)
        {
            return num1 % 3;
        }

        public static int Cube(int num1)
        {
            return num1 * num1 * num1;
        }
    }
}
